  private final String minecraftLocaleCode;
  private final String crowdinLocaleCode;
  private final String inGameName;
  public final static CrowdinLanguages DEFAULT_LANGUAGE = CrowdinLanguages.EN_US;

  CrowdinLanguages(String minecraftLocaleCode, @Nullable String crowdinLocaleCode, String inGameName) {
    this.minecraftLocaleCode = minecraftLocaleCode;
    this.crowdinLocaleCode = crowdinLocaleCode;
    this.inGameName = inGameName;
  }

  /**
   * Get the Minecraft locale code in "xx_xx" format.
   */
  public String getMinecraftLocaleCode() {
    return this.minecraftLocaleCode;
  }

  /**
   * Get the Crowdin locale code in "xx-XX" or "xx" formats.
   * The format depends on the language.
   */
  public String getCrowdinLocaleCode() {
    return this.crowdinLocaleCode;
  }

  /**
   * Return true if the current language is supported by Crowdin.
   */
  public boolean hasSupport() {
    return this.crowdinLocaleCode != null;
  }

  /**
   * Return the In-Game name of the language.
   */
  public String getInGameName() {
    return this.inGameName;
  }

  /**
   * Get the language instance by Minecraft or Crowdin locale codes.
   * If no language be found, the default language (EN_US) will be returned.
   * @param code Minecraft or Crowdin locale codes.
   * @return The language instance.
   */
  public static CrowdinLanguages getFromLocaleCode(String code) {
    for (CrowdinLanguages lang : CrowdinLanguages.values()) {
      if (lang.getMinecraftLocaleCode().equals(code) || lang.getCrowdinLocaleCode().equals(code) && lang.hasSupport()) {
        return lang;
      }
    }

    return CrowdinLanguages.DEFAULT_LANGUAGE;
  }
